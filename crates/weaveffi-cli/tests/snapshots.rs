fn fixture_path(file: &str) -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("tests/fixtures")
        .join(file)
}

fn load_api(file: &str) -> Api {
    let path = fixture_path(file);
    let contents = fs::read_to_string(&path)
        .unwrap_or_else(|e| panic!("read fixture {}: {e}", path.display()));
    let mut api = parse_api_str(&contents, "yaml")
        .unwrap_or_else(|e| panic!("parse fixture {}: {e}", path.display()));
    validate_api(&mut api, None)
        .unwrap_or_else(|e| panic!("validate fixture {}: {e}", path.display()));
    api
}

fn collect_files_sorted(root: &Path) -> Vec<PathBuf> {
    let mut out = Vec::new();
    walk(root, &mut out);
    out.sort();
    out
}

fn walk(dir: &Path, out: &mut Vec<PathBuf>) {
    let entries = match fs::read_dir(dir) {
        Ok(e) => e,
        Err(_) => return,
    };
    for entry in entries.flatten() {
        let path = entry.path();
        if path.is_dir() {
            walk(&path, out);
        } else if path.is_file() {
            out.push(path);
        }
    }
}

fn sanitize(rel: &Path) -> String {
    rel.to_string_lossy().replace(['/', '\\', '.', '-'], "_")
}

/// Replace the concrete crate version in every WeaveFFI prelude
/// (`Generated by WeaveFFI 0.5.0 from ...`) with a stable `[VERSION]`
/// placeholder. Without this, bumping the crate version invalidates all
/// 300+ generator snapshots even though no generated logic changed.
fn redact_version(s: &str) -> String {
    const MARKER: &str = "Generated by WeaveFFI ";
    let mut out = String::with_capacity(s.len());
    let mut rest = s;
    while let Some(idx) = rest.find(MARKER) {
        out.push_str(&rest[..idx + MARKER.len()]);
        rest = &rest[idx + MARKER.len()..];
        let end = rest.find(char::is_whitespace).unwrap_or(rest.len());
        out.push_str("[VERSION]");
        rest = &rest[end..];
    }
    out.push_str(rest);
    out
}

fn run_snapshot<G: Generator>(gen: &G, fixture_stem: &str, fixture_file: &str)
where
    G::Config: Default,
{
    let api = load_api(fixture_file);
    let tmp = tempfile::tempdir().expect("create tempdir");
    let out_dir = Utf8Path::from_path(tmp.path()).expect("utf8 tempdir");
    let config = G::Config::default();
    gen.generate(&api, out_dir, &config)
        .expect("generator failed");

    let gen_root = out_dir.join(gen.name());
    let files = collect_files_sorted(gen_root.as_std_path());
    assert!(
        !files.is_empty(),
        "generator {} produced no files for fixture {}",
        gen.name(),
        fixture_file,
    );

    insta::with_settings!({
        snapshot_path => "snapshots",
        prepend_module_to_snapshot => false,
        omit_expression => true,
    }, {
        for file in files {
            let rel = file
                .strip_prefix(gen_root.as_std_path())
                .expect("file under gen root");
            let name = format!("{}_{}__{}", gen.name(), fixture_stem, sanitize(rel));
            let contents = fs::read_to_string(&file)
                .unwrap_or_else(|e| panic!("read {}: {e}", file.display()));
            // Snapshot the version-redacted form so a release bump (e.g.
            // 0.5.0 → 0.6.0) does not invalidate every snapshot; assert the
            // real prelude (with the concrete version) against the original.
            insta::assert_snapshot!(name, redact_version(&contents));
            assert_prelude_present(&contents, &file);
        }
    });
}

/// Asserts every generated file embeds the standard WeaveFFI prelude markers
/// in its first five lines. JSON files (which cannot carry comments) embed
/// these markers via the `"//"` key convention.
fn assert_prelude_present(contents: &str, file: &Path) {
    let head: String = contents.lines().take(5).collect::<Vec<_>>().join("\n");
    assert!(
        head.contains("Generated by WeaveFFI"),
        "{} is missing 'Generated by WeaveFFI' in the first 5 lines:\n{head}",
        file.display(),
    );
    assert!(
        head.contains("DO NOT EDIT"),
        "{} is missing 'DO NOT EDIT' in the first 5 lines:\n{head}",
        file.display(),
    );
}

macro_rules! snapshot_test {
    ($fn_name:ident, $gen_expr:expr, $fixture_stem:literal, $fixture_file:literal) => {
        #[test]
        fn $fn_name() {
            run_snapshot(&$gen_expr, $fixture_stem, $fixture_file);
        }
    };
}

