/// Phase 5 prelude coverage: every generator, every fixture, every output file
/// must carry the standard WeaveFFI prelude markers in its first five lines.
#[test]
fn prelude_present_in_every_generated_file() {
    let generators: Vec<Box<dyn DynGenerator>> = vec![
        Box::new(ConfiguredGenerator::new(CGenerator, CConfig::default())),
        Box::new(ConfiguredGenerator::new(CppGenerator, CppConfig::default())),
        Box::new(ConfiguredGenerator::new(
            SwiftGenerator,
            SwiftConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(
            AndroidGenerator,
            AndroidConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(
            NodeGenerator,
            NodeConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(
            WasmGenerator,
            WasmConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(
            PythonGenerator,
            PythonConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(
            DotnetGenerator,
            DotnetConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(
            DartGenerator,
            DartConfig::default(),
        )),
        Box::new(ConfiguredGenerator::new(GoGenerator, GoConfig::default())),
        Box::new(ConfiguredGenerator::new(
            RubyGenerator,
            RubyConfig::default(),
        )),
    ];
    let fixtures = [
        "01_calculator.yml",
        "02_contacts.yml",
        "03_inventory.yml",
        "04_async_demo.yml",
        "05_events.yml",
        "06_kitchen_sink.yml",
        "07_docs_everywhere.yml",
        "08_kvstore.yml",
        "09_nested_modules.yml",
        "10_shapes.yml",
    ];

    for fixture in fixtures {
        let api = load_api(fixture);
        for gen in &generators {
            let tmp = tempfile::tempdir().expect("create tempdir");
            let out_dir = Utf8Path::from_path(tmp.path()).expect("utf8 tempdir");
            gen.generate(&api, out_dir).expect("generator failed");
            let gen_root = out_dir.join(gen.name());
            let files = collect_files_sorted(gen_root.as_std_path());
            assert!(
                !files.is_empty(),
                "generator {} produced no files for fixture {fixture}",
                gen.name(),
            );
            for file in files {
                let contents = fs::read_to_string(&file)
                    .unwrap_or_else(|e| panic!("read {}: {e}", file.display()));
                assert_prelude_present(&contents, &file);
            }
        }
    }
}
